class VarExerciseOne {
    public static void main (String args[]) {
        int integerVariable;
        double doubleVariable;
        String stringVariable;
        
        integerVariable = 256;
        doubleVariable = 3.1415;
        stringVariable = "I'm creating variables.";
         
        System.out.print("My favorite integer is ");
        System.out.println(integerVariable);
        System.out.print("A popular decimal is pi. It starts as ");
        System.out.println(doubleVariable);
        System.out.println(stringVariable);
    }
}
