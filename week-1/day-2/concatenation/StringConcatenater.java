public class StringConcatenater {
    public static void main(String[] args) {
        String words, moreWords;
        words = "";
        moreWords = "";

        words = words + "tom ";
        words = words + "purple ";
        words = words + "greece ";
        System.out.println(words);

        moreWords += "time ";
        moreWords += "check ";
        moreWords += "lobby ";
        System.out.println(moreWords);
    }
}